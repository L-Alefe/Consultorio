    private void atualizaTabela(){
         ControleSecretaria controle = new ControleSecretaria();
        		 ArrayList<ModeloPaciente> lista = new ArrayList<>();
        		 lista = controle.buscarPaciente();
			 DefaultTableModel tbm = (DefaultTableModel) buscaPaciente.getModel();
        		 while(tbm.getRowCount() > 0){
            			tbm.removeRow(0);
        		 }
                         int i = 0;
                         for(ModeloPaciente pac : lista){
                            tbm.addRow(new String[i]);
                            buscaPaciente.setValueAt(pac.getNome(), i, 0);
                            buscaPaciente.setValueAt(pac.getTelefone(), i, 1);
                            buscaPaciente.setValueAt(pac.getCpf(), i, 2);
                            buscaPaciente.setValueAt(pac.getRg(), i, 3);
                            buscaPaciente.setValueAt(pac.getEndereco(), i, 4);
                            buscaPaciente.setValueAt(pac.getSexo(), i, 5);
                            buscaPaciente.setValueAt(pac.getId(), i, 6);
                            i++;
	    }
    }

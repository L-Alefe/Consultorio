    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        ModeloPaciente paciente = new ModeloPaciente();
        paciente.setNome(campoNome.getText());
        paciente.setTelefone(campoTelefone.getText());
        paciente.setCpf(campoCpf.getText());
        paciente.setRg(campoRg.getText());
        paciente.setEndereco(campoEndereco.getText());
        paciente.setSexo(campoSexo.getText());
        ControleSecretaria controle = new ControleSecretaria();
        if(controle.cadastrarPacientes(paciente)){
            JOptionPane.showMessageDialog(null,"Paciente cadastrado");
            campoNome.setText("");
            campoTelefone.setText("");
            campoCpf.setText("");
            campoRg.setText("");
            campoEndereco.setText("");
            campoSexo.setText("");
        }else{
            JOptionPane.showMessageDialog(null,"Erro ao cadastrar", "Tente novamente!",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

